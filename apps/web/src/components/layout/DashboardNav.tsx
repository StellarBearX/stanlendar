    <svg fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" {...props}>
      <path strokeLinecap="round" strokeLinejoin="round" d="M6.75 3v2.25M17.25 3v2.25m-10.5 0h10.5v16.5H6.75V5.25zM8.25 7.5h7.5M8.25 12h7.5m-7.5 4.5h7.5" />
    </svg>
